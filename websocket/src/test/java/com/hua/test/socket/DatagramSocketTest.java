	@Test
	public void testClient() {
		try {
			InetAddress address = InetAddress.getByName(SERVER_ADDR);
			// 1.创建客户端UDP协议的Socket
			DatagramSocket socket = new DatagramSocket();
			byte[] request = "客户端请求Message".getBytes();
			// 2.创建数据包，发送给服务端的数据包
			DatagramPacket toServerPacket = new DatagramPacket(request, request.length, address, SERVER_PORT);
			// 发送数据给服务端端
			socket.send(toServerPacket);
			
			// 3.接收服务端发送的数据
			byte[] data = new byte[1024];
			DatagramPacket receiveServerPacket = new DatagramPacket(data, data.length);
			socket.receive(receiveServerPacket);
			// 4. 读取数据
			String response = new String(data);
			System.out.println("服务端响应数据: "  + response);

			// 关闭
			socket.close();
			
		} catch (Exception e) {
			log.error("testClient =====> ", e);
		}
	}
	

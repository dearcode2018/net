	//@DisplayName("test")
	@Test
	public void testServer() {
		try {
			// 1.创建服务端UDP协议的Socket
			DatagramSocket socket = new DatagramSocket(SERVER_PORT);
			byte[] data = new byte[1024];
			// 2.创建数据包，接收客户端发送的数据
			DatagramPacket receiveClientPacket = new DatagramPacket(data, 1024);
			// 3.接收客户端发送的数据
			socket.receive(receiveClientPacket);
			// 4. 读取数据
			String request = new String(data);
			System.out.println("客户端请求数据: "  + request);
			
			// 响应数据
			InetAddress clientAddress = receiveClientPacket.getAddress();
			int port = receiveClientPacket.getPort();
			byte[] response = "服务端响应Message".getBytes();
			DatagramPacket toClientPacket = new DatagramPacket(response, response.length, clientAddress, port);
			// 发送数据给客户端
			socket.send(toClientPacket);
			
			// 关闭
			socket.close();
			
		} catch (Exception e) {
			log.error("testServer =====> ", e);
		}
	}
	
